export const SQL_EDITOR_MESSAGES: Record<SqlEditorLocale, Record<string, string>> = {
    en: EN_MESSAGES,
    'zh-CN': ZH_MESSAGES,
}

export function snippetMessageKey(label: string, dialectVariant?: string): SqlEditorMessageKey | null {
    const normalized = label.toLowerCase()
    if (dialectVariant === 'sqlserver' && normalized === 'lim') return 'snippet.lim_sqlserver'
    const key = `snippet.${normalized}` as SqlEditorMessageKey
    return key in EN_MESSAGES ? key : null
}

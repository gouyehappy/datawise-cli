package org.apache.datawise.backend.domain;

import org.apache.datawise.backend.model.SemanticMetricEntry;

import java.util.List;

public record SemanticMetricDto(
        String id,
        String connectionId,
        String database,
        String name,
        String expression,
        String description,
        String unit,
        String owner,
        List<String> relatedTables,
        List<String> upstreamMetrics,
        Integer definitionVersion,
        Long definitionUpdatedAt,
        String latestChangeNote,
        boolean autoGenerated
) {
    public static SemanticMetricDto from(SemanticMetricEntry entry) {
        return new SemanticMetricDto(
                entry.getId(),
                entry.getConnectionId(),
                entry.getDatabase(),
                entry.getName(),
                entry.getExpression(),
                entry.getDescription(),
                entry.getUnit(),
                entry.getOwner(),
                entry.getRelatedTables(),
                entry.getUpstreamMetrics(),
                entry.getDefinitionVersion(),
                entry.getDefinitionUpdatedAt(),
                entry.getLatestChangeNote(),
                entry.isAutoGenerated()
        );
    }
}

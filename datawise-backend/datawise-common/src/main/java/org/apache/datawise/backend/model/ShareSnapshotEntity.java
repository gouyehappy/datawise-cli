package org.apache.datawise.backend.model;

import java.time.Instant;

/** Frozen read-only share snapshot (dashboard chart, etc.). */
public class ShareSnapshotEntity {

    public static final String KIND_DASHBOARD_CHART = "dashboard_chart";

    private String id;
    private String tenantId;
    private Long createdBy;
    private String title;
    private String kind = KIND_DASHBOARD_CHART;
    private String tokenHash;
    private String tokenLookup;
    private String payloadJson;
    private Instant expiresAt;
    private Instant createdAt;
    private boolean revoked;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTenantId() {
        return tenantId;
    }

    public void setTenantId(String tenantId) {
        this.tenantId = tenantId;
    }

    public Long getCreatedBy() {
        return createdBy;
    }

    public void setCreatedBy(Long createdBy) {
        this.createdBy = createdBy;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getKind() {
        return kind;
    }

    public void setKind(String kind) {
        this.kind = kind;
    }

    public String getTokenHash() {
        return tokenHash;
    }

    public void setTokenHash(String tokenHash) {
        this.tokenHash = tokenHash;
    }

    public String getTokenLookup() {
        return tokenLookup;
    }

    public void setTokenLookup(String tokenLookup) {
        this.tokenLookup = tokenLookup;
    }

    public String getPayloadJson() {
        return payloadJson;
    }

    public void setPayloadJson(String payloadJson) {
        this.payloadJson = payloadJson;
    }

    public Instant getExpiresAt() {
        return expiresAt;
    }

    public void setExpiresAt(Instant expiresAt) {
        this.expiresAt = expiresAt;
    }

    public Instant getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(Instant createdAt) {
        this.createdAt = createdAt;
    }

    public boolean isRevoked() {
        return revoked;
    }

    public void setRevoked(boolean revoked) {
        this.revoked = revoked;
    }
}
